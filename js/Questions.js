document.getElementById("quest1").addEventListener("click",function(id_que1){
    let question1 = id_que1.target;
    if (question1.classList[2] == "hidden")
    {
        document.getElementById("quest1").className = "h3-style color-a shown";
    }
    else
    {
        document.getElementById("quest1").className = "h3-style color-a hidden";
    }
},false);

